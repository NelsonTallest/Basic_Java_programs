package org.example;

public class AdditionClass {
    public static void main(String[] args) {
        int a = -5;
        int b = 8;
        int sum = a + b;
        System.out.println("\nThe sum of a and b is: " + sum);

        int product = a * b;

        System.out.println("\nThe product of a and b is: " + product);

    }
}
